# Semester grade: guarda 3 notas (numéricas), calcula el promedio y muestra un mensaje con el resultado.

note1 = 85 # Nota del primer examen
note2 = 90 # Nota del segundo examen
note3 = 78 # Nota del tercer examen

# Cálculo del promedio
average = (note1 + note2 + note3) / 3

# Mensaje de resultado
print(f"El promedio de {note1}, {note2} y {note3} es {average}.")