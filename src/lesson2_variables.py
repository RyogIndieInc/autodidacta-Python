# Ejemplo de variables en Python

# Entero (int)
edad = 29
# Número decimal (float)
altura = 1.75
# Cadena de texto (str)
nombre = "RyogDev"
# Booleano (bool)
es_estudiante = True

print("Edad:", edad)
print("Altura:", altura)
print("Nombre:", nombre)
print("¿Es estudiante?:", es_estudiante)

# Reasignación de variable
edad = edad + 1
print("Edad después de un año:", edad)
