# src/lesson4_numbers.py

# Entero (int)
vidas = 3
# Decimal (float)
energia = 75.5

print("Vidas iniciales:", vidas)
print("Energía inicial:", energia)

# Operaciones aritméticas
print("Suma:", 5 + 2)
print("Resta:", 10 - 3)
print("Multiplicación:", 4 * 3)
print("División real:", 7 / 2)   # devuelve float
print("División entera:", 7 // 2) # trunca el decimal
print("Módulo (residuo):", 7 % 2)
print("Exponente:", 2 ** 3)  # 2³

# Conversiones
numero = "100"
print("Número como string:", numero)
print("Número como int:", int(numero) + 50)
print("Número como float:", float(numero) + 0.5)

# Redondeo
print("Redondear 3.14159 a 2 decimales:", round(3.14159, 2))
